%Построение функции x1^2+x2^2
n=5;
%два вектора от 1 до 5 с шагом 1
x1 =1:1:5;
x2 = 1:1:5;
%нулевая матрица
y = zeros(n,n);
s=0;
%нулевая матрица для весов
Input = zeros(2,25);
%нулевой вектор 
Target = zeros(1,25);
