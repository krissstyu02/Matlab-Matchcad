for j=1:n
for i = 1:n
%заполняем матрицу y по формуле для вывода графика
y(i,j) = (x1(j) - 3)^2 + (x2(i)-3)^2 + k*(x1(j)-3)*(x2(i)-3);
s = s +1;
%создаем векторы входов
Input(1,s) = x1(j) - 3;
Input(2,s) = x2(i) - 3;
%получаем целевые значения для функции
Target(1,s) = (x1(j)-3)^2+(x2(i)-3)^2;
end
end
